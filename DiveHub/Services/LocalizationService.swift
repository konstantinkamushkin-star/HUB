    func localizedString(_ key: String, table: String = "common") -> String {
        let languageKey = currentLanguage.rawValue
        if let tableDict = translations[table] {
            if let languageDict = tableDict[languageKey],
               let translation = languageDict[key] {
                return translation
            }
            // Fallback to English when key is missing in current language
            if languageKey != "en",
               let englishDict = tableDict["en"],
               let translation = englishDict[key] {
                return translation
            }
        }

        // Fallback to key if translation not found
        return key
    }
