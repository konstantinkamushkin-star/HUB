    func localizedString(_ key: String, table: String = "common") -> String {
        let languageKey = currentLanguage.rawValue
        if let tableDict = translations[table],
           let languageDict = tableDict[languageKey],
           let translation = languageDict[key] {
            return translation
        }
        
        // Fallback to key if translation not found
        return key
    }
