import Foundation
import SwiftUI
import Combine

enum AppLanguage: String, CaseIterable {
    case english = "en"
    case russian = "ru"
    case spanish = "es"
    case german = "de"
    case french = "fr"
    case chinese = "zh"
    
    var displayName: String {
        switch self {
        case .english: return "English"
        case .russian: return "Русский"
        case .spanish: return "Español"
        case .german: return "Deutsch"
        case .french: return "Français"
        case .chinese: return "中文"
        }
    }
    
    var locale: Locale {
        Locale(identifier: rawValue)
    }
}

class LocalizationService: ObservableObject {
    static let shared = LocalizationService()
    
    @Published var currentLanguage: AppLanguage {
        didSet {
            UserDefaults.standard.set(currentLanguage.rawValue, forKey: "app_language")
            NotificationCenter.default.post(name: .languageChanged, object: nil)
            // Reload translations from backend when language changes
            Task {
                await self.loadTranslationsFromBackend()
            }
        }
    }
    
    private var translations: [String: [String: [String: String]]] = [:]
    
    // Load translations from backend API
    private func loadTranslationsFromBackend() async {
        do {
            let language = currentLanguage.rawValue
            let backendTranslations: [String: [String: String]] = try await NetworkService.shared.getTranslations(language: language)
            
            // Merge backend translations with local ones
            await MainActor.run {
                for (table, tableTranslations) in backendTranslations {
                    if self.translations[table] == nil {
                        self.translations[table] = [:]
                    }
                    self.translations[table]?[language] = tableTranslations
                }
            }
        } catch {
            // If backend fails, use local translations (already loaded)
            print("⚠️ Failed to load translations from backend: \(error)")
        }
    }
    
    private init() {
        // Load saved language or default to English
        if let savedLanguage = UserDefaults.standard.string(forKey: "app_language"),
           let language = AppLanguage(rawValue: savedLanguage) {
            self.currentLanguage = language
        } else {
            self.currentLanguage = .english
        }
        
        // Load local translations first (synchronous)
        loadLocalTranslations()
        
        // Load translations from backend (asynchronous, will merge with local)
        Task {
            await self.loadTranslationsFromBackend()
        }
    }
    
    private func loadLocalTranslations() {
        loadTranslations()
    }
    
